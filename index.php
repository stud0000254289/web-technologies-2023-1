function printNumbers() {
    $number = 0;
    do {
        if ($number === 0) {
            echo $number . " – это ноль.<br>";
        } elseif ($number % 2 === 0) {
            echo $number . " – чётное число.<br>";
        } else {
            echo $number . " – нечётное число.<br>";
        }
        $number++;
    } while ($number <= 10);
}

// Вызов функции для вывода чисел от 0 до 10 с указанием их чётности или нечётности
